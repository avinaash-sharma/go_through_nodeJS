const os = require('os');

//platform
console.log(os.platform());

//cpu Architecture
console.log(os.arch());

//cpu core info
console.log(os.cpus());

//Free memory
console.log(os.freemem());

//total memory
console.log(os.totalmem());

//home directory
console.log(os.homedir());

//os uptime
console.log(os.uptime());